    String titulos[] = {"ID", "Periodo", "FechaPago", "ID Alumno", "Alumno", "Cursos", "Cuota"};

    private void cargarTabla(List<Pago> lista) {
        //definir el modelo de la vista de la Tabla
        DefaultTableModel modeloTabla = new DefaultTableModel() {
            //filas y columnas no editables
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        //establecer nombres de columnas
        modeloTabla.setColumnIdentifiers(titulos);

        List<Pago> listaPagos = lista;

        //recorrer lista y mostrar los elementos de la tabla
        if (listaPagos != null) {
            for (Pago i : listaPagos) {
                //creamos el array de tipo object porque vamos a guardar diferentes tipos de datos
                Object[] objeto = {i.getId(), i.getPeriodo(), i.getFecha(), i.getAlumno().getId(), i.getAlumno().getNombre() + " " + i.getAlumno().getApellidos(), i.getAlumno().getNombreCursos(), i.getAlumno().getCuota()};

                modeloTabla.addRow(objeto);
            }
        }
        tbPagos.setModel(modeloTabla);
    }


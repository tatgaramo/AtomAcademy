    private void btEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditActionPerformed
        //controlar que la tabla no esté vacía
        if (tbPagos.getRowCount() > 0) {
            //controlar que haya un registro seleccionado
            if (tbPagos.getSelectedRow() != -1) {
                int id = (int) tbPagos.getValueAt(tbPagos.getSelectedRow(), 0);
                //llamda método buscar por ID
                Pago pago = pagoDAO.findById(id);

                //abrir el panel para editar el registro 
                panelEditarPago panel = new panelEditarPago(pago);

                JDialog dialogo = new JDialog(pantalla, "Editar Registro Pago", true);
                dialogo.setContentPane(panel);
                dialogo.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
                dialogo.pack();
                dialogo.setLocationRelativeTo(null);
                dialogo.setVisible(true);

            } else {
                aux.mostrarMensaje("No hay ningún registro seleccionado", "Error", "Error al eliminar");
            }
        } else {
            aux.mostrarMensaje("No hay registros para editar", "Error", "Error al eliminar");
        }

    }//GEN-LAST:event_btEditActionPerformed


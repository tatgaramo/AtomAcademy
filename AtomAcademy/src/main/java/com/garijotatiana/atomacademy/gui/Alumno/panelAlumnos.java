    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        try {
            //controlar que la tabla no estÃ© vacÃ­a
            if (tbAlumnos.getRowCount() > 0) {
                //controlar que haya un registro seleccionado
                if (tbAlumnos.getSelectedRow() != -1) {

                    int opcion = JOptionPane.showConfirmDialog(null, "Â¿Seguro que desea Eliminar el registro? ", "ConfirmaciÃ³n", JOptionPane.YES_NO_OPTION);

                    if (opcion == JOptionPane.YES_OPTION) {

                        int id = (int) tbAlumnos.getValueAt(tbAlumnos.getSelectedRow(), 0);
                        //llamda mÃ©todo buscar por ID
                        Alumno alumno = alumnoDAO.findById(id);
                        //comprobar si tiene pagos o notas asociadas
                        List<Pago> listaPagos = alumno.getPagos();
                        List<Nota> listaNotas = alumno.getNotas();

                        if (!listaPagos.isEmpty() && listaPagos != null) {
                            aux.mostrarMensaje("Este Alumno no se puede eliminar, tiene registros de pagos ", "Error", "Error al eliminar");
                        } else if (!listaNotas.isEmpty() && listaNotas != null) {
                            aux.mostrarMensaje("Este Alumno no se puede eliminar, tiene registros de notas ", "Error", "Error al eliminar");
                        } else {
                            //llamada al mÃ©todo para borrar el registro
                            alumnoDAO.delete(alumno);
                        }

                    } else {
                        aux.mostrarMensaje("Registro no eliminado", "Info", "Eliminar registro cancelado");
                    }

                } else {
                    aux.mostrarMensaje("No hay ningÃºn registro seleccionado", "Error", "Error al eliminar");
                }
            } else {
                aux.mostrarMensaje("No hay registros para elimiar", "Error", "Error al eliminar");
            }
        } catch (Exception ex) {
            aux.mostrarMensaje("Error", "Error", "Error al eliminar");
        }

        cargarTabla(alumnoDAO.findAll());
    }//GEN-LAST:event_btDeleteActionPerformed


    String titulos[] = {"ID", "Nombre", "Documento", "TelÃ©fono", "DirecciÃ³n", "Email", "Cursos", "Cuota"};

    private void cargarTabla(List<Alumno> lista) {
        //definir el modelo de la vista de la Tabla
        DefaultTableModel modeloTabla = new DefaultTableModel() {
            //filas y columnas no editables
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        //establecer nombres de columnas
        modeloTabla.setColumnIdentifiers(titulos);

        List<Alumno> listaAlumnos = lista;

        //recorrer lista y mostrar los elementos de la tabla
        if (listaAlumnos != null) {
            for (Alumno i : listaAlumnos) {
                //creamos el array de tipo object porque vamos a guardar diferentes tipos de datos
                Object[] objeto = {i.getId(), i.getNombre() + " " + i.getApellidos(), i.getDocumento(), i.getTelefono(), i.getDireccion(), i.getEmail(), i.getNombreCursos(), i.getCuota()};

                modeloTabla.addRow(objeto);
            }
        }
        tbAlumnos.setModel(modeloTabla);
    }

